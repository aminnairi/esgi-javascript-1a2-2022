// Définition d'une variable
let name = "Amin";

// Réaffectation d'une valeur à la variable
name = "Yves";

// Définition d'une constante
const age = 28;

// On peut commencer une variable par un underscore
let _private = true;

// On peut commencer une variable par une minuscule
let window = 1280;

// On peut également commencer une variable par une majuscule (même si c'est pas recommendé)
let Amin = "oui";

// On peut également commencer une variable par un dollar
const $observable = false;

// On peut avoir des nombres après le premier caractère
const file2md = "yes";
